// 面试题59（一）：滑动窗口的最大值
// 题目：给定一个数组和滑动窗口的大小，请找出所有滑动窗口里的最大值。例如，
// 如果输入数组{2, 3, 4, 2, 6, 2, 5, 1}及滑动窗口的大小3，那么一共存在6个
// 滑动窗口，它们的最大值分别为{4, 4, 6, 6, 6, 5}

// 解法: 双向队列. 先把尾部小于的数去掉, 再把前端下标小于窗口长度的去掉, 最后将当前数加入. 每次取队头作为窗口最大值.

#include "utils.h"

class Solution {
public:
	vector<int> maxInWindows(const vector<int>& num, unsigned int size) {
		int n = num.size();
		vector<int> res;
		if (n < size || size <= 0) return res;

		deque<int> index;

		for (int i = 0; i < n; i++) {
			while (!index.empty() && num[index.back()] <= num[i]) index.pop_back();
			while (!index.empty() && i - index.front() + 1 > size) index.pop_front();
			index.push_back(i);
			if (i+1 >= size) res.push_back(num[index.front()]);
		}

		return res;
	}	
};


// ====================测试代码====================
void Test(string testName, const vector<int>& num, unsigned int size, const vector<int>& expected)
{
    printf("%s begins: ", testName.c_str());

    Solution s = Solution();
    vector<int> result = s.maxInWindows(num, size);

    vector<int>::const_iterator iterResult = result.begin();
    vector<int>::const_iterator iterExpected = expected.begin();
    while(iterResult < result.end() && iterExpected < expected.end())
    {
        if(*iterResult != *iterExpected)
            break;

        ++iterResult;
        ++iterExpected;
    }

    if(iterResult == result.end() && iterExpected == expected.end())
        printf("Passed.\n");
    else
        printf("FAILED.\n");
}

void Test1()
{
    int num[] = { 2, 3, 4, 2, 6, 2, 5, 1 };
    vector<int> vecNumbers(num, num + sizeof(num) / sizeof(int));

    int expected[] = { 4, 4, 6, 6, 6, 5 };
    vector<int> vecExpected(expected, expected + sizeof(expected) / sizeof(int));

    unsigned int size = 3;

    Test("Test1", vecNumbers, size, vecExpected);
}

